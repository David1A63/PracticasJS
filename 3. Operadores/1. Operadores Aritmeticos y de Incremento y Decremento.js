//Sumas
let a = 3, b = 2;
let z = a + b;
console.log("El Resultado es: " + z);

//Restas
z = a - b;
console.log("El Resultado es: " + z);

//Multiplicación
z = a * b;
console.log("El Resultado es: " + z);

//División
z = a / b;
console.log("El Resultado es: " + z);

//Modulo o Residuo
z = a % b;
console.log("El Resultado es: " + z);

//Exponenciación (3^2)
z = a ** b;
console.log("El Resultado es: " + z);

//Incremento
//Pre-incremento (operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post-incremento (operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);

//Decremento
//Pre-decremento
z = --a;
console.log(a);
console.log(z);

//Post-decremento
z = b--;
console.log(b);
console.log(z);